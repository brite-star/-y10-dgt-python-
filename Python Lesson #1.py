# Python Lesson - Varibles 

#Varibles can be a string. 
Greetings = "Hello World"

#Varibles can be a number 
fav_num = 18 

#Varibles can be Boolean (True/False)
Love_Programming = True 

#Printing Greeting 
print(Greetings)

#Printing Fav_num
print (fav_num)

#Printing Love_Programming 
print(Love_Programming) 

#New Varible
num_string = "5"

#Printing fav_num *2
print(fav_num * 2)

#Printing numstring*5 
print(num_string*5) 

